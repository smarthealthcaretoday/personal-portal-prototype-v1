  MENTALHEALTH_ID            BIGINT,
  METABOLICDISORDERS_ID      BIGINT,
  NERVOUSSYSTEM_ID           BIGINT,
  OTHERCHRONICDISEASES_ID    BIGINT,
  RESPIRATORYSYSTEM_ID       BIGINT,
  SKINDISORDERS_ID           BIGINT,
  TRAUMAS_ID                 BIGINT,
  TREATMENTUPTONOW_ID        BIGINT,
  UROGENITALSYSTEM_ID        BIGINT,
  PRIMARY KEY (ID) USING BTREE,
  FOREIGN KEY (PATIENT_ID) REFERENCES PATIENTS (ID),
  FOREIGN KEY (LIFESTYLE_ID) REFERENCES HEALTHSTATEMENTLIFESTYLE (ID),
  FOREIGN KEY (MEDICALDEVICES_ID) REFERENCES HEALTHSTATEMENTMEDICALDEVICES (ID),
  FOREIGN KEY (CARDIOVASCULARCONDITION_ID) REFERENCES HEALTHSTATEMENTCARDIOVASCULARCONDITION (ID),
  FOREIGN KEY (WORKINGENVIRONMENT_ID) REFERENCES HEALTHSTATEMENTWORKINGENVIRONMENT (ID),
  FOREIGN KEY (ALLERGIES_ID) REFERENCES HEALTHSTATEMENTALLERGIES (ID),
  FOREIGN KEY (BLOODPROBLEMS_ID) REFERENCES HEALTHSTATEMENTBLOODPROBLEMS (ID),
  FOREIGN KEY (BONESJOINTSMUSCLES_ID) REFERENCES HEALTHSTATEMENTBONESJOINTSMUSCLES (ID),
  FOREIGN KEY (DIGESTIVEORGANS_ID) REFERENCES HEALTHSTATEMENTDIGESTIVEORGANS (ID),
  FOREIGN KEY (EARNOSETHROAT_ID) REFERENCES HEALTHSTATEMENTEARNOSETHROAT (ID),
  FOREIGN KEY (EYESANDVISION_ID) REFERENCES HEALTHSTATEMENTEYESANDVISION (ID),
  FOREIGN KEY (INFECTIOUSDISEASES_ID) REFERENCES HEALTHSTATEMENTINFECTIOUSDISEASES (ID),
  FOREIGN KEY (MENTALHEALTH_ID) REFERENCES HEALTHSTATEMENTMENTALHEALTH (ID),
  FOREIGN KEY (METABOLICDISORDERS_ID) REFERENCES HEALTHSTATEMENTMETABOLICDISORDERS (ID),
  FOREIGN KEY (NERVOUSSYSTEM_ID) REFERENCES HEALTHSTATEMENTNERVOUSSYSTEM (ID),
