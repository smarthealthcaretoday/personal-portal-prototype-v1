CREATE TABLE HEALTHSTATEMENT (
  ID                         BIGINT NOT NULL AUTO_INCREMENT,
  PATIENT_ID                 BIGINT,
  LIFESTYLE_ID               BIGINT,
  MEDICALDEVICES_ID          BIGINT,
  CARDIOVASCULARCONDITION_ID BIGINT,
  WORKINGENVIRONMENT_ID      BIGINT,
  ALLERGIES_ID               BIGINT,
  BLOODPROBLEMS_ID           BIGINT,
  BONESJOINTSMUSCLES_ID      BIGINT,
  DIGESTIVEORGANS_ID         BIGINT,
  EARNOSETHROAT_ID           BIGINT,
